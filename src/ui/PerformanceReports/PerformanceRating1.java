    private ArrayList<UserAccount> findTop3Employees() {
        HashMap<UserAccount, Integer> empCount = new HashMap<>();
        for(WorkRequest request : this.business.getAppLogEmpOrganization().getWorkQueue().getWorkRequestList()){
            if(!empCount.containsKey(request.getReceiver())){
               empCount.put(request.getReceiver(), 1);
            }
            else{
              empCount.put(request.getReceiver(), empCount.get(request.getReceiver())+1);  
            }   
        }
        ArrayList<UserAccount> empList = this.business.getAppLogEmpOrganization().getUserAccountDirectory().getUserAccountList();
        Collections.sort(empList, new UserMapComparator(empCount));
    
    return empList;
    }

