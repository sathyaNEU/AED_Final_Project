    private void config() {
      String today = new SimpleDateFormat("dd/MM/YYYY").format(new Date());
      DailyPricing dp = this.business.getDailyPricingList().getDailyPricingList(today);
      this.dateTextField.setText(today);
      if(dp==null){
          this.expPriceTextField.setText("Not Configured");
          this.priorityPriceTextField.setText("Not Configured");
      }
      else{
         this.expPriceTextField.setText(String.valueOf(dp.getExpressPrice()));
         this.priorityPriceTextField.setText(String.valueOf(dp.getPriorityPrice()));
      }
    }
