    private void assignTextBox() {
        this.top1TF.setText("Not Assigned");
        this.top2TF.setText("Not Assigned");
        this.top3TF.setText("Not Assigned");
        int counter=0;
        for(int i=0; i<findTop3Employees().size();i++){
            if(i==0)
                this.top1TF.setText(findTop3Employees().get(i).getEmployee().getName());
            else if(i==1)
                this.top2TF.setText(findTop3Employees().get(i).getEmployee().getName());
            else if(i==2)
                this.top3TF.setText(findTop3Employees().get(i).getEmployee().getName());
            counter+=1;
            if(counter==3)
                break;
        }
    }
