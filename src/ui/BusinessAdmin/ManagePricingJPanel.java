    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
      try{
      DailyPricing dp = new DailyPricing();
      dp.setDate(new Date());
      String today = dp.getDate();
      float expPriceF = Float.parseFloat(this.expPriceTextField.getText());
      //String expPrice = String.valueOf(expPriceF);
      float priPriceF  = Float.parseFloat(this.priorityPriceTextField.getText());
      //String priPrice = String.valueOf(priPrice);    
      dp.setExpressPrice(expPriceF);
      dp.setPriorityPrice(priPriceF);
      this.business.getDailyPricingList().setDailyPricing(today, dp);
      JOptionPane.showMessageDialog(this, "Prices set successfully");
      config();
      }
      catch(Exception e){
          e.printStackTrace();
          ErrorHelper.showError("No other characters expected other than 0-9");
      }


        
    }//GEN-LAST:event_jButton1ActionPerformed

